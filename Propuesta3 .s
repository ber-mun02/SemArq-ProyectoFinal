#Programa para saber la suma de ciertos números consecutivos
#Ejemplo utilizado con números del 1 al 20

add $21, $20, $1            #$21 = $20 + $1 // $21 = 20 + 1 = 21
mult $21, $21, $20          #$21 = $21 * $20 // $21 = 21 * 20 = 420
div $22, $21, $2            #$22 = $21 / $2 // $22 = 21 / 2 = 210